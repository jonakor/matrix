

def text_to_roll(text):
	rollsize = 200
	roll = [[0]*rollsize]*5
	for i in range(5):
		for j in range(rollsize):
			roll[i][j] = 0

	index = 10
	text.upper()
	for i in range(len(text)):
		charsize = getCharSize(text[i])
		if (index + charsize) > (rollsize-10):
			break

		roll[0:5][index:index+charsize] = getChar(text[i])
		index += charsize

	return roll[0:5][0:index+10]


def getCharSize(c):
	if c == 'I' or c == ' ' or c == '!':
		return 2
	elif c == 'N':
		return 5
	elif c == 'M' or c == 'Q' or c == 'W':
		return 6
	else:
		return 4

