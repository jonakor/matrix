

def text_to_roll(text):
	rollsize = 200
	roll = [[0 for i in range(rollsize)] for j in range(5)]

	index = 10
	text = text.upper()
	for i in range(len(text)):
		charsize = getCharSize(text[i])
		if (index + charsize) > (rollsize-12):
			break

		character = getChar(text[i])
		for j in range(5):
			roll[j][index:index + charsize] = character[j][:]
		
		index += charsize

	out = [[0 for i in range(index + 12)] for j in range(5)]
	for i in range(5):
		out[i] = roll[i][0:index + 12]
		
	return out


def getCharSize(c):
	if c == 'I' or c == ' ' or c == '!':
		return 2
	elif c == 'N':
		return 5
	elif c == 'M' or c == 'Q' or c == 'W':
		return 6
	else:
		return 4

